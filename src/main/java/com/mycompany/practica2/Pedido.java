    public void MostrarDatos(Producto producto){
        modelo.insertRow(con, new Object[]{});
        modelo.setValueAt(producto.getNombre(), con, 0);
        modelo.setValueAt(producto.getPrecio(), con, 1);
        con++;
    }
    
